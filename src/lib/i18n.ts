export function getTranslations(locale: Locale): Messages {
  const loc = LOCALES.includes(locale as Locale) ? (locale as Locale) : DEFAULT_LOCALE;
  return messages[loc] as Messages;
}

/** Normalize a locale string (cookie value, form field, etc.) to a supported `Locale`. */
export function parseLocale(value: string | undefined): Locale {
  if (value && LOCALES.includes(value as Locale)) return value as Locale;
  return DEFAULT_LOCALE;
}

/** Replace {planName} in deletePlanConfirmMessage. */
export function formatDeletePlanMessage(message: string, planName: string): string {
  return message.replace("{planName}", planName);
}

/** Format "X of Y task(s) completed" for plan list. */
export function formatTasksCount(
  tasksCountZero: string,
  tasksCountOne: string,
  tasksCountMany: string,
  total: number,
  completed: number,
): string {
  if (total === 0) return tasksCountZero;
  const template = total === 1 ? tasksCountOne : tasksCountMany;
  return template.replace("{{completed}}", String(completed)).replace("{{total}}", String(total));
}
