#!/usr/bin/env node
/**
 * Parses CHANGELOG.md (changesets format) and writes src/lib/recent-updates.generated.ts
 * so the About page "Recent updates" section stays in sync. Run at build time or after
 * changeset version.
 */

import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const changelogPath = join(root, "CHANGELOG.md");
const outPath = join(root, "src", "lib", "recent-updates.generated.ts");

const changelog = readFileSync(changelogPath, "utf8").replace(/\r\n/g, "\n");

/** @type {{ version: string; entries: string[] }[]} */
const recentUpdates = [];

// Match "## X.Y.Z" version headers and content until next ## or end
const versionBlocks = changelog.split(/\n## (?=\d)/);
for (const block of versionBlocks) {
  const versionMatch = block.match(/^(\d+\.\d+\.\d+)/m);
  if (!versionMatch) continue;
  const version = versionMatch[1];
  const entries = [];

  // Collect list items under ### Minor Changes or ### Patch Changes
  const lines = block.split("\n");
  let inChanges = false;
  for (const line of lines) {
    const trimmed = line.trim();
    if (/^### (Minor|Patch) Changes\s*$/.test(trimmed)) {
      inChanges = true;
      continue;
    }
    if (inChanges) {
      if (trimmed.startsWith("##") || (trimmed.startsWith("###") && !trimmed.includes("Changes"))) {
        inChanges = false;
        continue;
      }
      const bullet = line.match(/^\s*-\s+(.+)$/);
      if (bullet) {
        // Strip "hash: " prefix (e.g. "7cd7aef: message" -> "message")
        const raw = bullet[1].trim();
        const message = raw.replace(/^[a-f0-9]+:\s*/i, "").trim() || raw;
        entries.push(message);
      }
    }
  }

  if (entries.length > 0) {
    recentUpdates.push({ version, entries });
  }
}

const tsContent = `// Generated by scripts/sync-about-changelog.mjs — do not edit by hand.
// Source: CHANGELOG.md (updated when changeset version runs; build runs this script).

export const RECENT_UPDATES: { version: string; entries: string[] }[] = ${JSON.stringify(recentUpdates, null, 2)};
`;

writeFileSync(outPath, tsContent, "utf8");
console.log("Wrote", outPath, "(" + recentUpdates.length + " versions)");
